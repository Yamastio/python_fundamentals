angka_1 = 5
angka_2 = 2

# Pertambahan
pertambahan = angka_1 + angka_2
print(pertambahan)

# Pegurangan
pegurangan = angka_1 - angka_2
print(pegurangan)

# Perkalian
perkalian = angka_1 * angka_2
print(perkalian)

# Pembagian menghasilkan bilangan float
pembagian = angka_1 / angka_2
print(pembagian)

# Pembagian menghasilkan bilangan bulat / integer
pembagian = angka_1 // angka_2
print(pembagian)

# Modulus == sisa dari pembagian
modulus = angka_1 % angka_2
print(modulus)

# Perpangkatan
pangkat = angka_1 ** angka_2
print(pangkat)

panjang = 20
lebar = 12
hasil = panjang * lebar
print(hasil)

# Hitung umur
tahun_sekarang = 2025
tahun_lahir = 2003
umur = tahun_sekarang - tahun_lahir
print(umur)


