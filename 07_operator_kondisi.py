# Operator kondisi == operator untuk mengecek kondisi

# 1. Persamaan
print("Apakah 2 == 2 = ", 2 == 2)
print("Apakah 2 == 3 = ", 2 == 3)

# 2. Pertidaksaman
print("Apakah 2 != 2 = ", 2 != 2)
print("Apakah 2 != 3 = ", 2 != 3)

# 3. Lebih besar
print("Apakah 2 > 2 = ", 2 > 2)
print("Apakah 2 > 3 = ", 2 > 3)

# 4. Lebih kecil
print("Apakah 2 < 2 = ", 2 < 2)
print("Apakah 2 < 3 = ", 2 < 3)

# 5. Lebih besar atau sama dengan
print("Apakah 2 >= 2 = ", 2 >= 2)
print("Apakah 2 >= 3 = ", 2 >= 3)

# 6. Lebih kecil atau sama dengan
print("Apakah 2 <= 2 = ", 2 <= 2)
print("Apakah 2 <= 3 = ", 2 <= 3)


