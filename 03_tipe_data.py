# tipe data = sebuah informasi yang disimpan dalam sebuah variabel
# tipe data primitive = tipe data yang bersifat dasar

# Tipe data numerik
bilangan_bulat = 15
bilangan_koma = 3.14
bilangan_kompleks = 8.2j

print(bilangan_bulat)
print(type(bilangan_bulat))

print(bilangan_koma)
print(type(bilangan_koma))

print(bilangan_kompleks)
print(type(bilangan_kompleks))

# Tipe data string
nama = "Yamastio"

print(nama)
print(type(nama))

# Tipe data Boolean
Benar = True
Salah = False

print(Benar)
print(type(Benar))

print(Salah)
print(type(Salah))

